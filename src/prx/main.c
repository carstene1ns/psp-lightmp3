#include <pspsdk.h>
#include <pspkernel.h>
#include <pspdisplay_kernel.h>
#include <pspimpose_driver.h>
#include <pspsysreg.h>
#include <pspctrl.h>

PSP_MODULE_INFO("supportlib", 0x1006, 1, 1);
PSP_MAIN_THREAD_ATTR(0);

int sceDisplayEnable(void);
int sceDisplayDisable(void);

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Globals
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
u32 k1;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//getBrightness
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int getBrightness(){
    int currentBrightness = 0;
    k1 = pspSdkSetK1(0);
    sceDisplayGetBrightness(&currentBrightness, 0);
    pspSdkSetK1(k1);
    return currentBrightness;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//setBrightness
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void setBrightness(int brightness){
    k1 = pspSdkSetK1(0);
    sceDisplaySetBrightness(brightness, 0);
    pspSdkSetK1(k1);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//imposeGetBrightness
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int imposeGetBrightness(){
    k1 = pspSdkSetK1(0);
    int ret = sceImposeGetParam(PSP_IMPOSE_BACKLIGHT_BRIGHTNESS);
    pspSdkSetK1(k1);
    return ret;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//imposeSetBrightness
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int imposeSetBrightness(int value){
    k1 = pspSdkSetK1(0);
    int ret = sceImposeSetParam(PSP_IMPOSE_BACKLIGHT_BRIGHTNESS, value);
    pspSdkSetK1(k1);
    return ret;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//imposeGetVolume
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int imposeGetVolume(){
    k1 = pspSdkSetK1(0);
    int ret = sceImposeGetParam(PSP_IMPOSE_MAIN_VOLUME);
    pspSdkSetK1(k1);
    return ret;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//displayEnable
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int displayEnable(void){
    k1 = pspSdkSetK1(0);
    int ret = sceDisplayEnable();
    pspSdkSetK1(k1);
    return ret;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//displayDisable
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int displayDisable(void){
    k1 = pspSdkSetK1(0);
    int ret = sceDisplayDisable();
    pspSdkSetK1(k1);
    return ret;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//MEDisable
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void MEDisable(void){
    k1 = pspSdkSetK1(0);
    sceSysregMeBusClockDisable();
    pspSdkSetK1(k1);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Read PSP Buttons
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void initController(){
    k1 = pspSdkSetK1(0);
	sceCtrlSetSamplingCycle(0);
    sceCtrlSetSamplingMode(PSP_CTRL_MODE_ANALOG);
    pspSdkSetK1(k1);
}

void readButtons(SceCtrlData *pad_data, int count){
    k1 = pspSdkSetK1(0);
	sceCtrlReadBufferPositive(pad_data, count);
    pspSdkSetK1(k1);
}


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//module_start
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int module_start(SceSize args, void *argp)
{
    initController();
	return 0;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//module_stop
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int module_stop()
{
	return 0;
}
